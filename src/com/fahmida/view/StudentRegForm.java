    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = "", email = "", age = "0", subject = "";
        if (fName.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Enter your name");
        } else if (!CheckEmailValidity(fEmail.getText()) || fEmail.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Enter your Email");
        } else if (Integer.parseInt(fAge.getText()) < 18 || Integer.parseInt(fAge.getText()) > 72) {
            JOptionPane.showMessageDialog(null, "Enter your Age");
        } else if (fSubject.getItemAt(fSubject.getSelectedIndex()) == "Select a Subject") {

            JOptionPane.showMessageDialog(null, "Select a Subject");
        } else {
            name = fName.getText();
            email = fEmail.getText();
            age = fAge.getText();
            subject = fSubject.getItemAt(fSubject.getSelectedIndex());
            StudentReq sr = new StudentReq(name, email, Integer.parseInt(age), subject);
            //    List<StudentRegForm> s = new ArrayList<>();
            service = new StudentRegService();
            service.save(sr);

        }
        
        this.setVisible(false);
        new OnlineTest().setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

